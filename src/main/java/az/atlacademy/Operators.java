package az.atlacademy;

public class Operators {
    public static void main(String[] args) {
        int a=5;
        int b =1;
        System.out.println(a+b);
        System.out.println(b-a);
        System.out.println(a/b);
        System.out.println(b%a);
        System.out.println(b*a);
        System.out.println(b++);
        System.out.println(a--);
    }
}
